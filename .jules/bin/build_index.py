import json
from pathlib import Path


def read_json(path: Path) -> dict:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    runs_dir = repo_root / ".jules" / "runs"
    output_file = repo_root / ".jules" / "index" / "generated" / "RUNS_ROLLUP.md"

    runs = []

    # 1. Historical Docs Ledger
    docs_ledger_path = repo_root / ".jules" / "docs" / "ledger.json"
    try:
        docs_ledger = json.loads(docs_ledger_path.read_text(encoding="utf-8"))
        if isinstance(docs_ledger, list):
            for run in docs_ledger:
                runs.append({
                    "id": run.get("run_id", "Unknown"),
                    "persona": "Unknown",
                    "style": "Unknown",
                    "shard": run.get("lane", "Unknown"),
                    "status": "success",
                    "gates_run": len(run.get("receipts", [])),
                })
    except (FileNotFoundError, json.JSONDecodeError):
        pass

    # 2. Historical Quality Ledger
    quality_ledger_path = repo_root / ".jules" / "quality" / "ledger.json"
    quality_ledger = read_json(quality_ledger_path)
    if isinstance(quality_ledger, dict) and "runs" in quality_ledger:
        for run in quality_ledger["runs"]:
            run_id = run.get("run_id", "Unknown")
            envelope = read_json(repo_root / ".jules" / "quality" / "envelopes" / f"{run_id}.json")

            # extract persona if present, otherwise check run description
            persona = envelope.get("persona")
            if not persona:
                desc = run.get("description", "")
                if "Gatekeeper" in desc:
                    persona = "Gatekeeper"
                else:
                    persona = "Unknown"

            runs.append({
                "id": run_id,
                "persona": persona,
                "style": "Unknown",
                "shard": "Unknown",
                "status": "success",
                "gates_run": len(envelope.get("receipts", [])) if isinstance(envelope, dict) else 0,
            })

    # 3. Current Runs
    if runs_dir.exists():
        for run_path in runs_dir.iterdir():
            if not run_path.is_dir():
                continue

            envelope = read_json(run_path / "envelope.json")
            result = read_json(run_path / "result.json")

            runs.append({
                "id": run_path.name,
                "persona": envelope.get("persona") or "Unknown",
                "style": envelope.get("style") or "Unknown",
                "shard": envelope.get("primary_shard") or "Unknown",
                "status": result.get("status") or "in-progress",
                "gates_run": len(result.get("gates_run", [])),
            })

    # Sort runs by ID
    runs.sort(key=lambda x: x["id"])

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Run Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up metadata from all run packets in `.jules/runs/` and historical ledgers.\n\n")

        f.write("| Run ID | Persona | Style | Shard | Status | Gates Run |\n")
        f.write("|---|---|---|---|---|---|\n")

        for r in runs:
            f.write(f"| `{r['id']}` | {r['persona']} | {r['style']} | {r['shard']} | {r['status']} | {r['gates_run']} |\n")

if __name__ == "__main__":
    main()
