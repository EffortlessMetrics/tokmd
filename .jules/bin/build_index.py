import json
from pathlib import Path


def read_json(path: Path) -> dict | list:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    output_file = repo_root / ".jules" / "index" / "generated" / "RUNS_ROLLUP.md"

    runs = []

    # 1. Live runs
    runs_dir = repo_root / ".jules" / "runs"
    if runs_dir.exists():
        for run_path in sorted(runs_dir.iterdir(), key=lambda path: path.name):
            if not run_path.is_dir():
                continue

            envelope = read_json(run_path / "envelope.json")
            if not isinstance(envelope, dict):
                envelope = {}
            result = read_json(run_path / "result.json")
            if not isinstance(result, dict):
                result = {}

            runs.append({
                "id": run_path.name,
                "persona": envelope.get("persona") or "Unknown",
                "style": envelope.get("style") or "Unknown",
                "shard": envelope.get("primary_shard") or "Unknown",
                "status": result.get("status") or "in-progress",
                "gates_run": len(result.get("gates_run", [])),
                "source": "live"
            })

    # 2. Historical Docs Ledger
    docs_ledger_path = repo_root / ".jules" / "docs" / "ledger.json"
    docs_ledger = read_json(docs_ledger_path)
    if isinstance(docs_ledger, list):
        for entry in docs_ledger:
            if not isinstance(entry, dict):
                continue

            run_id = entry.get("run_id", "Unknown")
            env_path = repo_root / ".jules" / "docs" / "envelopes" / f"{run_id}.json"
            env_data = read_json(env_path) if env_path.exists() else {}
            if not isinstance(env_data, dict):
                env_data = {}

            persona = env_data.get("persona") or entry.get("persona") or entry.get("lane") or "Unknown"
            style = env_data.get("style") or entry.get("style") or "Unknown"
            shard = env_data.get("primary_shard") or entry.get("target") or "Unknown"
            gates_run = len(env_data.get("receipts", entry.get("receipts", [])))

            runs.append({
                "id": run_id,
                "persona": persona,
                "style": style,
                "shard": shard,
                "status": "historical",
                "gates_run": gates_run,
                "source": "docs"
            })

    # 3. Historical Quality Ledger
    quality_ledger_path = repo_root / ".jules" / "quality" / "ledger.json"
    quality_ledger = read_json(quality_ledger_path)
    if isinstance(quality_ledger, dict) and "runs" in quality_ledger:
        for entry in quality_ledger["runs"]:
            if not isinstance(entry, dict):
                continue

            run_id = entry.get("run_id", "Unknown")
            env_path = repo_root / ".jules" / "quality" / "envelopes" / f"{run_id}.json"
            env_data = read_json(env_path) if env_path.exists() else {}
            if not isinstance(env_data, dict):
                env_data = {}

            desc = entry.get("description", "")
            persona = env_data.get("persona") or "Unknown"
            if persona == "Unknown" and "Gatekeeper" in desc:
                persona = "Gatekeeper"

            style = env_data.get("style") or "Unknown"
            shard = env_data.get("primary_shard") or entry.get("target") or "Unknown"
            gates_run = len(env_data.get("receipts", entry.get("receipts", [])))

            runs.append({
                "id": run_id,
                "persona": persona,
                "style": style,
                "shard": shard,
                "status": "historical",
                "gates_run": gates_run,
                "source": "quality"
            })

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Run Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up metadata from all run packets in `.jules/runs/` and historical ledgers.\n\n")

        f.write("| Run ID | Persona | Style | Shard | Status | Gates Run | Source |\n")
        f.write("|---|---|---|---|---|---|---|\n")

        for r in runs:
            f.write(f"| `{r['id']}` | {r['persona']} | {r['style']} | {r['shard']} | {r['status']} | {r['gates_run']} | {r['source']} |\n")

    # 4. Generate Friction Rollup
    friction_output_file = repo_root / ".jules" / "index" / "generated" / "FRICTION_ROLLUP.md"
    friction_dir = repo_root / ".jules" / "friction" / "open"
    frictions = []

    if friction_dir.exists():
        for item_path in sorted(friction_dir.glob("*.md")):
            fric_id = "Unknown"
            persona = "Unknown"
            style = "Unknown"
            shard = "Unknown"
            status = "Unknown"

            try:
                text = item_path.read_text(encoding="utf-8")
                for line in text.splitlines():
                    line = line.strip()
                    if line.startswith("id:"):
                        fric_id = line[3:].strip()
                    elif line.startswith("persona:"):
                        persona = line[8:].strip()
                    elif line.startswith("style:"):
                        style = line[6:].strip()
                    elif line.startswith("shard:"):
                        shard = line[6:].strip()
                    elif line.startswith("status:"):
                        status = line[7:].strip()
            except Exception:
                pass

            # fallback if id is not in frontmatter
            if fric_id == "Unknown":
                fric_id = item_path.stem

            frictions.append({
                "id": fric_id,
                "persona": persona,
                "style": style,
                "shard": shard,
                "status": status,
                "file": item_path.name
            })

    with friction_output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Friction Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up metadata from all open friction items in `.jules/friction/open/`.\n\n")

        f.write("| ID | Persona | Style | Shard | Status | File |\n")
        f.write("|---|---|---|---|---|---|\n")

        for r in frictions:
            f.write(f"| `{r['id']}` | {r['persona']} | {r['style']} | {r['shard']} | {r['status']} | `{r['file']}` |\n")

if __name__ == "__main__":
    main()
