import json
from pathlib import Path


def read_json(path: Path) -> dict | list:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    runs_dir = repo_root / ".jules" / "runs"
    docs_ledger = repo_root / ".jules" / "docs" / "ledger.json"
    output_file = repo_root / ".jules" / "index" / "generated" / "RUNS_ROLLUP.md"

    runs = []
    if runs_dir.exists():
        for run_path in sorted(runs_dir.iterdir(), key=lambda path: path.name):
            if not run_path.is_dir():
                continue

            envelope = read_json(run_path / "envelope.json")
            if not isinstance(envelope, dict):
                envelope = {}
            result = read_json(run_path / "result.json")
            if not isinstance(result, dict):
                result = {}

            runs.append({
                "id": run_path.name,
                "persona": envelope.get("persona") or "Unknown",
                "style": envelope.get("style") or "Unknown",
                "shard": envelope.get("primary_shard") or "Unknown",
                "status": result.get("status") or "in-progress",
                "gates_run": len(result.get("gates_run", [])),
            })

    if docs_ledger.exists():
        historical_runs = read_json(docs_ledger)
        if isinstance(historical_runs, list):
            for entry in historical_runs:
                if not isinstance(entry, dict):
                    continue
                runs.append({
                    "id": entry.get("run_id") or "Unknown",
                    "persona": entry.get("persona") or "Unknown",
                    "style": entry.get("style") or "Unknown",
                    "shard": entry.get("lane") or "Unknown",
                    "status": "historical",
                    "gates_run": len(entry.get("receipts", [])),
                })

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Run Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up metadata from all run packets in `.jules/runs/` and historical runs in `.jules/docs/`.\n\n")

        f.write("| Run ID | Persona | Style | Shard | Status | Gates Run |\n")
        f.write("|---|---|---|---|---|---|\n")

        for r in runs:
            f.write(f"| `{r['id']}` | {r['persona']} | {r['style']} | {r['shard']} | {r['status']} | {r['gates_run']} |\n")

if __name__ == "__main__":
    main()
