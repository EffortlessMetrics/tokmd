import json
from pathlib import Path
from typing import Any

def read_json(path: Path) -> Any:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return {}

def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    runs_dir = repo_root / ".jules" / "runs"
    output_file = repo_root / ".jules" / "index" / "generated" / "RUNS_ROLLUP.md"

    runs = []

    # Process standard .jules/runs packets
    if runs_dir.exists():
        for run_path in sorted(runs_dir.iterdir(), key=lambda path: path.name):
            if not run_path.is_dir():
                continue

            envelope = read_json(run_path / "envelope.json")
            if not isinstance(envelope, dict): envelope = {}
            result = read_json(run_path / "result.json")
            if not isinstance(result, dict): result = {}

            runs.append({
                "id": run_path.name,
                "persona": envelope.get("persona") or "Unknown",
                "style": envelope.get("style") or "Unknown",
                "shard": envelope.get("primary_shard") or "Unknown",
                "status": result.get("status") or "in-progress",
                "gates_run": len(result.get("gates_run", [])),
                "source": "runs"
            })

    # Aggregate historical ledgers (.jules/docs/ledger.json)
    docs_ledger_path = repo_root / ".jules" / "docs" / "ledger.json"
    docs_ledger = read_json(docs_ledger_path)
    if isinstance(docs_ledger, list):
        for run in docs_ledger:
            if isinstance(run, dict):
                lane = str(run.get("lane", "Unknown"))
                persona = lane.capitalize() if lane != "Unknown" else "Unknown"
                runs.append({
                    "id": run.get("run_id", "Unknown"),
                    "persona": persona,
                    "style": "Unknown",
                    "shard": "Unknown",
                    "status": "historical",
                    "gates_run": len(run.get("receipts", [])),
                    "source": "docs"
                })

    # Aggregate historical ledgers (.jules/quality/ledger.json)
    quality_ledger_path = repo_root / ".jules" / "quality" / "ledger.json"
    quality_ledger = read_json(quality_ledger_path)
    if isinstance(quality_ledger, dict) and "runs" in quality_ledger:
        for run in quality_ledger["runs"]:
            if isinstance(run, dict):
                desc = run.get("description", "")
                persona = "Unknown"
                if "Gatekeeper" in desc:
                    persona = "Gatekeeper"
                elif "Sentinel" in desc:
                    persona = "Sentinel"
                runs.append({
                    "id": run.get("run_id", "Unknown"),
                    "persona": persona,
                    "style": "Unknown",
                    "shard": "Unknown",
                    "status": "historical",
                    "gates_run": 0,
                    "source": "quality"
                })

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Run Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up metadata from all run packets in `.jules/runs/` and historical ledgers (`.jules/docs/` and `.jules/quality/`).\n\n")

        f.write("| Run ID | Persona | Style | Shard | Status | Gates Run | Source |\n")
        f.write("|---|---|---|---|---|---|---|\n")

        runs.sort(key=lambda r: r["id"])

        for r in runs:
            f.write(f"| `{r['id']}` | {r['persona']} | {r['style']} | {r['shard']} | {r['status']} | {r['gates_run']} | {r['source']} |\n")

if __name__ == "__main__":
    main()
